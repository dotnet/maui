using System;
using System.Collections.Generic;
using System.IO;
using System.Text;

namespace Microsoft.Maui.Controls.SourceGen.Css;

/// <summary>
/// Generates C# source code for a compiled CSS stylesheet factory.
/// </summary>
static class CssCodeGenerator
{
	/// <summary>
	/// Generates a C# source file that creates a pre-parsed <see cref="StyleSheet"/> for the given CSS rules.
	/// </summary>
	public static string Generate(IReadOnlyList<CssRule> rules, IReadOnlyList<CssMediaGroup> mediaGroups, string? manifestResourceName, string? targetPath)
	{
		// Resolve var() references at compile time within this file's scope
		var variables = CssVariableResolver.CollectVariables(rules);
		var resolvedRules = CssVariableResolver.ResolveRules(rules, variables);

		// Also resolve variables inside media groups
		var resolvedMediaGroups = new List<CssMediaGroup>(mediaGroups.Count);
		foreach (var mg in mediaGroups)
		{
			var resolvedMgRules = CssVariableResolver.ResolveRules(mg.Rules, variables);
			resolvedMediaGroups.Add(new CssMediaGroup(mg.Condition, resolvedMgRules));
		}

		var sb = new StringBuilder();
		sb.AppendLine(GeneratorHelpers.AutoGeneratedHeaderText);
		sb.AppendLine("#nullable enable");
		sb.AppendLine();

		// Keep the XamlResourceId attribute for backwards compat (FromResource still works)
		if (manifestResourceName != null && targetPath != null)
		{
			sb.AppendLine($"[assembly: global::Microsoft.Maui.Controls.Xaml.XamlResourceId(\"{manifestResourceName}\", \"{targetPath.Replace('\\', '/')}\", null)]");
			sb.AppendLine();
		}

		// Generate the compiled stylesheet factory
		string safeClassName = MakeSafeClassName(targetPath ?? manifestResourceName ?? "UnknownCss");
		string registrationKey = targetPath?.Replace('\\', '/') ?? manifestResourceName ?? "";

		sb.AppendLine("namespace Microsoft.Maui.Controls.StyleSheets");
		sb.AppendLine("{");
		sb.AppendLine($"    [global::System.ComponentModel.EditorBrowsable(global::System.ComponentModel.EditorBrowsableState.Never)]");
		sb.AppendLine($"    internal static class {safeClassName}");
		sb.AppendLine("    {");
		sb.AppendLine("        private static global::Microsoft.Maui.Controls.StyleSheets.StyleSheet? _instance;");
		sb.AppendLine();
		sb.AppendLine("        [global::System.Runtime.CompilerServices.ModuleInitializer]");
		sb.AppendLine("        internal static void Register()");
		sb.AppendLine("        {");
		sb.AppendLine($"            global::Microsoft.Maui.Controls.StyleSheets.StyleSheet.RegisterCompiled(\"{EscapeString(registrationKey)}\", GetStyleSheet);");
		sb.AppendLine("        }");
		sb.AppendLine();
		sb.AppendLine("        internal static global::Microsoft.Maui.Controls.StyleSheets.StyleSheet GetStyleSheet()");
		sb.AppendLine("        {");
		sb.AppendLine("            if (_instance != null) return _instance;");

		// Emit base rules
		if (resolvedRules.Count == 0)
		{
			sb.Append("            var __rules = global::System.Array.Empty<global::Microsoft.Maui.Controls.StyleSheets.CompiledCssRule>()");
		}
		else
		{
			sb.AppendLine($"            var __rules = new global::Microsoft.Maui.Controls.StyleSheets.CompiledCssRule[]");
			sb.AppendLine("            {");
			EmitRules(sb, resolvedRules, "                ");
			sb.Append("            }");
		}
		sb.AppendLine(";");

		// Emit media groups
		if (resolvedMediaGroups.Count > 0)
		{
			sb.AppendLine($"            var __mediaGroups = new global::Microsoft.Maui.Controls.StyleSheets.CompiledCssMediaGroup[]");
			sb.AppendLine("            {");

			for (int m = 0; m < resolvedMediaGroups.Count; m++)
			{
				var mg = resolvedMediaGroups[m];
				sb.AppendLine($"                new global::Microsoft.Maui.Controls.StyleSheets.CompiledCssMediaGroup(\"{EscapeString(mg.Condition)}\", new global::Microsoft.Maui.Controls.StyleSheets.CompiledCssRule[]");
				sb.AppendLine("                {");
				EmitRules(sb, mg.Rules, "                    ");
				sb.Append("                })");
				if (m < resolvedMediaGroups.Count - 1)
					sb.AppendLine(",");
				else
					sb.AppendLine();
			}

			sb.AppendLine("            };");
			sb.AppendLine("            _instance = global::Microsoft.Maui.Controls.StyleSheets.StyleSheet.CreateCompiled(__rules, __mediaGroups);");
		}
		else
		{
			sb.AppendLine("            _instance = global::Microsoft.Maui.Controls.StyleSheets.StyleSheet.CreateCompiled(__rules);");
		}

		sb.AppendLine("            return _instance;");
		sb.AppendLine("        }");
		sb.AppendLine("    }");
		sb.AppendLine("}");

		return sb.ToString();
	}

	static void EmitRules(StringBuilder sb, IReadOnlyList<CssRule> rules, string indent)
	{
		for (int i = 0; i < rules.Count; i++)
		{
			var rule = rules[i];
			sb.Append($"{indent}new global::Microsoft.Maui.Controls.StyleSheets.CompiledCssRule(\"{EscapeString(rule.Selector)}\"");

			if (rule.Declarations.Count == 0)
			{
				sb.Append(", global::System.Array.Empty<global::System.Collections.Generic.KeyValuePair<string, string>>()");
			}
			else
			{
				sb.AppendLine(",");
				sb.AppendLine($"{indent}    new global::System.Collections.Generic.KeyValuePair<string, string>[]");
				sb.AppendLine($"{indent}    {{");

				for (int j = 0; j < rule.Declarations.Count; j++)
				{
					var decl = rule.Declarations[j];
					sb.Append($"{indent}        new global::System.Collections.Generic.KeyValuePair<string, string>(\"{EscapeString(decl.Key)}\", \"{EscapeString(decl.Value)}\")");
					if (j < rule.Declarations.Count - 1)
						sb.AppendLine(",");
					else
						sb.AppendLine();
				}

				sb.Append($"{indent}    }}");
			}

			// Emit ImportantProperties array if any
			if (rule.ImportantProperties != null && rule.ImportantProperties.Count > 0)
			{
				sb.Append(", new string[] { ");
				bool first = true;
				foreach (var prop in rule.ImportantProperties)
				{
					if (!first) sb.Append(", ");
					sb.Append($"\"{EscapeString(prop)}\"");
					first = false;
				}
				sb.Append(" }");
			}

			sb.Append(")");
			if (i < rules.Count - 1)
				sb.AppendLine(",");
			else
				sb.AppendLine();
		}
	}

	static string MakeSafeClassName(string path)
	{
		var sb = new StringBuilder("__CompiledCss_");
		foreach (var c in Path.GetFileNameWithoutExtension(path))
		{
			if (char.IsLetterOrDigit(c))
				sb.Append(c);
			else
				sb.Append('_');
		}
		return sb.ToString();
	}

	static string EscapeString(string s)
	{
		return s.Replace("\\", "\\\\").Replace("\"", "\\\"").Replace("\n", "\\n").Replace("\r", "\\r").Replace("\t", "\\t");
	}
}
