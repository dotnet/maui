namespace Microsoft.Maui.Controls.Handlers.Items.Platform
{
    /// <summary>
    /// Enumerates values that specifies how snap points are aligned with items.
    /// </summary>
	public enum SnapPointsAlignment
	{
        /// <summary>
        /// indicates that scrolling item was aligned with start
        /// </summary>
		Start,
        /// <summary>
        /// indicates that scrolling item was aligned with center
        /// </summary>
		Center,
        /// <summary>
        /// indicates that scrolling item was aligned with end
        /// </summary>
		End
    }
}