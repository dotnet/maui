namespace Microsoft.Maui.Controls
{
	/// <summary>
	/// Specifies the shape of indicators in an <see cref="IndicatorView"/>.
	/// </summary>
	public enum IndicatorShape
	{
		/// <summary>
		/// Indicators are displayed as circles.
		/// </summary>
		Circle,

		/// <summary>
		/// Indicators are displayed as squares.
		/// </summary>
		Square
	}
}