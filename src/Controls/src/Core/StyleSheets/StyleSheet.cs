using System;
using System.Collections.Concurrent;
using System.Collections.Generic;
using System.ComponentModel;
using System.IO;
using System.Reflection;
using System.Runtime.CompilerServices;
using System.Xml;

using Microsoft.Maui.ApplicationModel;
using Microsoft.Maui.Controls.Xaml;

namespace Microsoft.Maui.Controls.StyleSheets
{
	/// <summary>
	/// Represents a CSS-like stylesheet for styling visual elements.
	/// </summary>
	public sealed class StyleSheet : IStyle
	{
		static readonly ConcurrentDictionary<string, Func<StyleSheet>> s_compiledFactories = new();

		StyleSheet()
		{
		}

		internal IDictionary<Selector, Style> Styles { get; set; } = new Dictionary<Selector, Style>();

		/// <summary>CSS custom properties (variables) extracted from all rules in this stylesheet.</summary>
		internal Dictionary<string, string> Variables { get; private set; }

		/// <summary>List of @import URLs found in this stylesheet.</summary>
		internal List<string> Imports { get; private set; }

		/// <summary>Conditional @media rule groups. Evaluated reactively against window size / theme.</summary>
		internal List<MediaGroup> MediaGroups { get; private set; }

		/// <summary>Represents a group of styles gated by a @media condition.</summary>
		internal sealed class MediaGroup
		{
			public string Condition { get; }
			public IDictionary<Selector, Style> Styles { get; }
			public bool IsActive { get; set; }

			public MediaGroup(string condition, IDictionary<Selector, Style> styles)
			{
				Condition = condition;
				Styles = styles;
			}
		}

		/// <summary>Registers a compiled stylesheet factory. Used by source-generated code.</summary>
		[EditorBrowsable(EditorBrowsableState.Never)]
		public static void RegisterCompiled(string key, Func<StyleSheet> factory)
			=> s_compiledFactories[key] = factory;

		/// <summary>Creates a <see cref="StyleSheet"/> from pre-parsed rules. Used by source-generated code.</summary>
		[EditorBrowsable(EditorBrowsableState.Never)]
		public static StyleSheet CreateCompiled(CompiledCssRule[] rules)
			=> CreateCompiled(rules, null);

		/// <summary>Creates a <see cref="StyleSheet"/> from pre-parsed rules and media groups. Used by source-generated code.</summary>
		[EditorBrowsable(EditorBrowsableState.Never)]
		public static StyleSheet CreateCompiled(CompiledCssRule[] rules, CompiledCssMediaGroup[] mediaGroups)
		{
			var sheet = new StyleSheet();
			PopulateStyles(sheet, rules);

			if (mediaGroups != null && mediaGroups.Length > 0)
			{
				sheet.MediaGroups = new List<MediaGroup>(mediaGroups.Length);
				foreach (var mg in mediaGroups)
				{
					var styles = new Dictionary<Selector, Style>();
					var tempSheet = new StyleSheet { Styles = styles };
					PopulateStyles(tempSheet, mg.Rules);
					sheet.MediaGroups.Add(new MediaGroup(mg.Condition, styles));
				}
			}

			return sheet;
		}

		static void PopulateStyles(StyleSheet sheet, CompiledCssRule[] rules)
		{
			if (rules == null) return;
			foreach (var rule in rules)
			{
				var selector = Selector.Parse(rule.Selector);

				var style = new Style();
				if (rule.Declarations != null)
				{
					foreach (var decl in rule.Declarations)
						style.Declarations[decl.Key] = decl.Value;
				}
				if (rule.ImportantProperties != null && rule.ImportantProperties.Length > 0)
				{
					style.ImportantDeclarations = new System.Collections.Generic.HashSet<string>(StringComparer.Ordinal);
					foreach (var prop in rule.ImportantProperties)
						style.ImportantDeclarations.Add(prop);
				}
				sheet.Styles[selector] = style;
				ExtractVariables(sheet, style);
			}
		}

		//used by code generated by XamlC. Has to stay public
		/// <summary>Creates a <see cref="StyleSheet"/> from an embedded resource.</summary>
		[EditorBrowsable(EditorBrowsableState.Never)]
		public static StyleSheet FromResource(string resourcePath, Assembly assembly, IXmlLineInfo lineInfo = null)
		{
			if (s_compiledFactories.TryGetValue(resourcePath, out var factory))
				return factory();

			var styleSheet = new StyleSheet();
			var resString = GetResource(resourcePath, assembly, styleSheet, lineInfo);
			using (var textReader = new StringReader(resString))
			using (var cssReader = new CssReader(textReader))
				Parse(styleSheet, cssReader);
			return styleSheet;
		}

		//used by code generated by XamlC and SourceGen. Has to stay public
		/// <summary>Creates a <see cref="StyleSheet"/> from a CSS string.</summary>
		public static StyleSheet FromString(string stylesheet)
		{
			if (stylesheet == null)
				throw new ArgumentNullException(nameof(stylesheet));
			using (var reader = new StringReader(stylesheet))
				return FromReader(reader);
		}

		/// <summary>Creates a <see cref="StyleSheet"/> from a <see cref="TextReader"/>.</summary>
		public static StyleSheet FromReader(TextReader reader)
		{
			if (reader == null)
				throw new ArgumentNullException(nameof(reader));

			var sheet = new StyleSheet();
			using (var cssReader = new CssReader(reader))
				Parse(sheet, cssReader);
			return sheet;
		}

		[MethodImpl(MethodImplOptions.AggressiveInlining)]
		static void Parse(StyleSheet sheet, CssReader reader)
		{
			Style style = null;
			var selector = Selector.All;

			int p;
			bool inStyle = false;
			reader.SkipWhiteSpaces();
			while ((p = reader.Peek()) > 0)
			{
				switch ((char)p)
				{
					case '@':
						reader.Read(); // consume '@'
						var keyword = reader.ReadIdent();
						if (string.Equals(keyword, "import", StringComparison.OrdinalIgnoreCase))
						{
							reader.SkipWhiteSpaces();
							var importUrl = ReadImportUrl(reader);
							// Skip to end of statement
							reader.ReadUntil(';');
							if (reader.Peek() == ';')
								reader.Read();

							if (!string.IsNullOrEmpty(importUrl))
							{
								sheet.Imports ??= new List<string>();
								sheet.Imports.Add(importUrl);
							}
						}
						else if (string.Equals(keyword, "media", StringComparison.OrdinalIgnoreCase))
						{
							reader.SkipWhiteSpaces();
							// Read condition text up to '{'
							var condition = reader.ReadUntil('{');
							if (reader.Peek() == '{')
								reader.Read(); // consume '{'

							// Parse inner rules into a media group
							var mediaStyles = new Dictionary<Selector, Style>();
							ParseMediaBlock(reader, mediaStyles);

							if (mediaStyles.Count > 0)
							{
								sheet.MediaGroups ??= new List<MediaGroup>();
								sheet.MediaGroups.Add(new MediaGroup(condition?.Trim(), mediaStyles));
							}
						}
						else if (string.Equals(keyword, "charset", StringComparison.OrdinalIgnoreCase))
						{
							// @charset "UTF-8"; — skip to semicolon
							reader.ReadUntil(';');
							if (reader.Peek() == ';')
								reader.Read();
						}
						else
						{
							// Skip unsupported at-rules (e.g., @keyframes)
							SkipAtRule(reader);
						}
						break;
					case '{':
						reader.Read();
						style = Style.Parse(reader, '}');
						inStyle = true;
						break;
					case '}':
						reader.Read();
						if (!inStyle)
							throw new Exception();
						inStyle = false;
						sheet.Styles.Add(selector, style);
						ExtractVariables(sheet, style);
						style = null;
						selector = Selector.All;
						break;
					default:
						reader.SkipWhiteSpaces();
						if (reader.Peek() == '@')
							continue; // re-enter loop to handle @-rule
						if (reader.Peek() <= 0)
							break;
						selector = Selector.Parse(reader, '{');
						break;
				}
			}
		}

		static string ReadImportUrl(CssReader reader)
		{
			int p = reader.Peek();
			if (p == '"' || p == '\'')
			{
				// @import "file.css";
				var quote = (char)reader.Read();
				var url = reader.ReadUntil(quote);
				if (reader.Peek() == quote)
					reader.Read();
				return url;
			}
			// @import url("file.css"); or @import url(file.css);
			var ident = reader.ReadIdent();
			if (string.Equals(ident, "url", StringComparison.OrdinalIgnoreCase) && reader.Peek() == '(')
			{
				reader.Read(); // consume '('
				reader.SkipWhiteSpaces();
				p = reader.Peek();
				string url;
				if (p == '"' || p == '\'')
				{
					var quote = (char)reader.Read();
					url = reader.ReadUntil(quote);
					if (reader.Peek() == quote)
						reader.Read();
				}
				else
				{
					url = reader.ReadUntil(')');
				}
				reader.SkipWhiteSpaces();
				if (reader.Peek() == ')')
					reader.Read();
				return url;
			}
			return null;
		}

		static void SkipAtRule(CssReader reader)
		{
			// Skip to either ';' (statement) or matching '{...}' (block)
			int depth = 0;
			int p;
			while ((p = reader.Read()) > 0)
			{
				var c = (char)p;
				if (c == '{')
					depth++;
				else if (c == '}')
				{
					if (depth <= 1)
						return;
					depth--;
				}
				else if (c == ';' && depth == 0)
					return;
			}
		}

		/// <summary>Parses CSS rules inside a @media block until the closing '}'.</summary>
		static void ParseMediaBlock(CssReader reader, IDictionary<Selector, Style> styles)
		{
			Style style = null;
			var selector = Selector.All;
			bool inStyle = false;

			reader.SkipWhiteSpaces();
			int p;
			while ((p = reader.Peek()) > 0)
			{
				switch ((char)p)
				{
					case '}':
						reader.Read();
						if (inStyle)
						{
							// End of inner rule
							inStyle = false;
							styles[selector] = style;
							style = null;
							selector = Selector.All;
						}
						else
						{
							// End of @media block
							return;
						}
						break;
					case '{':
						reader.Read();
						style = Style.Parse(reader, '}');
						inStyle = true;
						break;
					default:
						reader.SkipWhiteSpaces();
						if (reader.Peek() <= 0 || reader.Peek() == '}')
							break;
						selector = Selector.Parse(reader, '{');
						break;
				}
			}
		}

		/// <summary>Extracts custom property declarations (--*) from a style into the stylesheet's variable store.</summary>
		static void ExtractVariables(StyleSheet sheet, Style style)
		{
			foreach (var decl in style.Declarations)
			{
				if (decl.Key.StartsWith("--", StringComparison.Ordinal))
				{
					sheet.Variables ??= new Dictionary<string, string>(StringComparer.Ordinal);
					sheet.Variables[decl.Key] = decl.Value;
				}
			}
		}

		Type IStyle.TargetType => typeof(VisualElement);

		void IStyle.Apply(BindableObject bindable, SetterSpecificity setterspecificity)
		{
			if (!(bindable is Element styleable))
				return;
			Apply(styleable, mergedVariables: null);
		}

		/// <summary>
		/// Applies this stylesheet to the given element, resolving var() references against the merged variable scope.
		/// </summary>
		internal void Apply(Element styleable, IDictionary<string, string> mergedVariables)
		{
			if (!(styleable is VisualElement visualStylable))
				return;

			// Apply base (unconditional) styles
			foreach (var kvp in Styles)
			{
				var selector = kvp.Key;
				var style = kvp.Value;

				// Check if this selector contains a pseudo-class
				if (PseudoClassStyleHandler.TryExtractPseudoClass(selector, out var pseudoClass, out var remaining))
				{
					// For pseudo-class selectors, we apply to VSM instead of direct property sets
					if (remaining.Matches(styleable))
					{
						PseudoClassStyleHandler.ApplyPseudoClassStyle(visualStylable, pseudoClass, style, 
							Selector.SelectorSpecificity.FromSelector(remaining), mergedVariables);
					}
				}
				else if (selector.Matches(styleable))
				{
					// Regular (non-pseudo-class) selector
					style.Apply(visualStylable, Selector.SelectorSpecificity.FromSelector(selector), variables: mergedVariables);
				}
			}

			// Apply active @media conditional styles
			if (MediaGroups != null)
			{
				foreach (var mg in MediaGroups)
				{
					if (!mg.IsActive)
						continue;
					foreach (var kvp in mg.Styles)
					{
						var selector = kvp.Key;
						var style = kvp.Value;

						// Check if this selector contains a pseudo-class
						if (PseudoClassStyleHandler.TryExtractPseudoClass(selector, out var pseudoClass, out var remaining))
						{
							if (remaining.Matches(styleable))
							{
								PseudoClassStyleHandler.ApplyPseudoClassStyle(visualStylable, pseudoClass, style,
									Selector.SelectorSpecificity.FromSelector(remaining), mergedVariables);
							}
						}
						else if (selector.Matches(styleable))
						{
							style.Apply(visualStylable, Selector.SelectorSpecificity.FromSelector(selector), variables: mergedVariables);
						}
					}
				}
			}
		}

		/// <summary>
		/// Evaluates all @media conditions against the current window size and theme,
		/// returning true if any condition changed state (requiring style re-application).
		/// </summary>
		internal bool EvaluateMediaQueries(double windowWidth, double windowHeight, AppTheme appTheme)
		{
			if (MediaGroups == null || MediaGroups.Count == 0)
				return false;

			bool anyChanged = false;
			foreach (var mg in MediaGroups)
			{
				bool nowActive = MediaQueryEvaluator.Evaluate(mg.Condition, windowWidth, windowHeight, appTheme);
				if (nowActive != mg.IsActive)
				{
					mg.IsActive = nowActive;
					anyChanged = true;
				}
			}
			return anyChanged;
		}

		void IStyle.UnApply(BindableObject bindable) => throw new NotImplementedException();

		private static string GetResource(string resourcePath, Assembly assembly, object target, IXmlLineInfo lineInfo)
		{
			var resourceLoadingResponse = Maui.Controls.Internals.ResourceLoader.ResourceProvider2?.Invoke(new Maui.Controls.Internals.ResourceLoader.ResourceLoadingQuery
			{
				AssemblyName = assembly.GetName(),
				ResourcePath = resourcePath,
				Instance = target
			});

			var alternateResource = resourceLoadingResponse?.ResourceContent;
			if (alternateResource != null)
				return alternateResource;

			var resourceId = XamlResourceIdAttribute.GetResourceIdForPath(assembly, resourcePath);
			if (resourceId == null)
				throw new XamlParseException($"Resource '{resourcePath}' not found.", lineInfo);

			using (var stream = assembly.GetManifestResourceStream(resourceId))
			{
				if (stream == null)
					throw new XamlParseException($"No resource found for '{resourceId}'.", lineInfo);
				using (var reader = new StreamReader(stream))
					return reader.ReadToEnd();
			}
		}
	}
