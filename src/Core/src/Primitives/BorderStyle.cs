namespace Microsoft.Maui
{
    /// <summary>
    /// Enumerates values that control the border style of an Entry control.
    /// </summary>
    public enum BorderStyle
    {
        /// <summary>
        /// Shows the platform's default border/decoration style.
        /// </summary>
        Default,

        /// <summary>
        /// Hides the border/decoration (borderless style).
        /// </summary>
        None
    }
}
