namespace Microsoft.Maui
{
    /// <summary>
    /// Specifies the placement of the tab bar within a tabbed view.
    /// </summary>
    public enum TabBarPlacement
    {
        /// <summary>
        /// Tabs are placed at the top of the view.
        /// </summary>
        Top = 0,

        /// <summary>
        /// Tabs are placed at the bottom of the view.
        /// </summary>
        Bottom = 1
    }
}
